#ifndef STDINT_H_
#define STDINT_H_

/* Abstractions over integer data types. */

/* Signed types. */

typedef char  int8_t;
typedef short int16_t;
typedef int   int32_t;
typedef long  int64_t;

/* Unsigned types. */

typedef unsigned char  uint8_t;
typedef unsigned short uint16_t;
typedef unsigned int   uint32_t;
typedef unsigned long  uint64_t;

#endif
